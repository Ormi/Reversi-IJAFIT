    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // Prevzatie jednotlivych hodnot zo spinnerov pre freeze funkiu
        freezeDisks = (int) frozenDisks.getValue();
        unfreeze = ((long) unfreezeLen.getValue()) * 1000000000;
        freeze = ((int) freezeLen.getValue()) * 1000;

        if (jCheckBox1.isSelected() == true)
            freezeEnable = true;
        else
            freezeEnable = false;
        
        // Vytvorenie hry
        ReversiGame timo = new ReversiGame(frameSize, AIdifficulty, freezeEnable, freezeDisks, unfreeze, freeze, null);
    }//GEN-LAST:event_jButton1ActionPerformed

